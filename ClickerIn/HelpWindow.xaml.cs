using System.Windows;
using System.Windows.Documents;
using System.Windows.Media;

namespace ClickerIn
{
    public partial class HelpWindow : Window
    {
        public HelpWindow()
        {
            InitializeComponent();
            BuildHelp();
        }

        private void BuildHelp()
        {
            var doc = RtbHelp.Document;
            doc.Blocks.Clear();
            var accent = new SolidColorBrush(Color.FromRgb(0x8E, 0xA1, 0xE6));
            accent.Freeze();
            var fg = Brushes.White;
            var dim = Brushes.LightGray;

            void AddSection(string title, string[] lines)
            {
                var header = new Paragraph(new Run(title) { FontWeight = FontWeights.Bold, FontSize = 15, Foreground = accent });
                header.Margin = new Thickness(0, 12, 0, 4);
                doc.Blocks.Add(header);

                foreach (var line in lines)
                {
                    var p = new Paragraph(new Run(line) { Foreground = dim, FontSize = 13 });
                    p.Margin = new Thickness(8, 1, 0, 1);
                    doc.Blocks.Add(p);
                }
            }

            var intro = new Paragraph(new Run(
                "ClickerIn — это автокликер, который запоминает ваши клики мышкой и может повторять их автоматически. " +
                "Программа работает в фоновом режиме и управляется через компактный оверлей поверх всех окон.")
            { Foreground = fg });
            intro.Margin = new Thickness(0, 0, 0, 8);
            doc.Blocks.Add(intro);

            AddSection("🚀 Быстрый старт", new[]
            {
                "1. Откройте оверлей (кнопка «Оверлей» в панели или двойной клик по иконке в трее).",
                "2. Выберите сценарий из списка или создайте новый.",
                "3. Нажмите «⏺ Запись» — программа начнёт записывать все ваши клики.",
                "4. Кликайте в нужных местах нужной программы.",
                "5. Нажмите «⏹ Стоп» — запись остановится.",
                "6. Нажмите «▶ Пуск» — программа повторит все записанные клики.",
                "",
                "Всё! Программа запомнила, В КАКОЙ ПРОГРАММЕ вы кликали, и при повторе",
                "сама найдёт и откроет это окно."
            });

            AddSection("🖥 Оверлей", new[]
            {
                "Оверлей — это маленькое окошко, которое всегда поверх всех окон.",
                "Через него можно делать ВСЁ: запускать, останавливать, записывать.",
                "Перетаскивайте его за заголовок в удобное место.",
                "Кнопка «✕» свернёт в трей (не закроет!)."
            });

            AddSection("🎯 Привязка к программе", new[]
            {
                "При записи ClickerIn запоминает, в каком окне был каждый клик.",
                "При воспроизведении программа сама:",
                "  • Найдёт нужное окно",
                "  • Развернёт его, если свёрнуто",
                "  • Кликнет в правильном месте (даже если окно сдвинулось)",
                "",
                "В столбце «Программа» таблицы шагов видно, к какому процессу привязан клик."
            });

            AddSection("⏰ Расписание", new[]
            {
                "Можно настроить автоматический запуск сценария по расписанию:",
                "  • Каждый день в определённое время (например, в 14:00)",
                "  • Однократно в конкретную дату и время",
                "  • По определённым дням недели",
                "",
                "Перед запуском программа проверит, открыто ли нужное окно:",
                "  — Если окно открыто: покажет уведомление с обратным отсчётом (5 мин).",
                "    Можно нажать «Продолжить» или «Отложить».",
                "  — Если свёрнуто: развернёт автоматически.",
                "  — Если закрыто: покажет сообщение «откройте программу вручную».",
                "",
                "Кнопка «⏰» в оверлее или «➕» в разделе расписаний основного окна."
            });


            AddSection("⌨ Горячие клавиши", new[]
            {
                "Ctrl+F8 — запуск/остановка сценария (работает глобально)",
                "Ctrl+F9 — начать/остановить запись",
                "",
                "Горячие клавиши можно переназначить в настройках (⚙)."
            });

            AddSection("🔧 Работа в фоне", new[]
            {
                "ClickerIn продолжает работать, даже когда окно закрыто.",
                "Иконка в системном трее (рядом с часами) — управление программой.",
                "Двойной клик по иконке — открыть главное окно.",
                "Правый клик — меню (Открыть / Оверлей / Выход).",
                "",
                "Чтобы ПОЛНОСТЬЮ закрыть программу — нажмите «Выход» в меню трея."
            });

            AddSection("💡 Советы", new[]
            {
                "• Не двигайте мышку слишком быстро во время записи.",
                "• Перед первым запуском — проверьте шаги в таблице.",
                "• Если окно целевой программы сдвинулось — сценарий всё равно сработает.",
                "• Сценарии автоматически сохраняются в папку Scenarios при закрытии."
            });
        }

        private void Close_Click(object sender, RoutedEventArgs e) => Close();
    }
}